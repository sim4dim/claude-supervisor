#!/usr/bin/env node
/**
 * Eval Housekeeping — analyzes eval-history.jsonl to find patterns that
 * should be auto-approved by the hook, generates dynamic rules.
 * Run by supervisor server 2x/day, or manually: node scripts/eval-housekeeping.js
 */

import { readFileSync, writeFileSync, existsSync, appendFileSync } from 'fs';
import { join, dirname } from 'path';
import { fileURLToPath } from 'url';

const __dirname = dirname(fileURLToPath(import.meta.url));
const ROOT = join(__dirname, '..');
const HISTORY_FILE = join(ROOT, 'logs', 'eval-history.jsonl');
const DYNAMIC_RULES_FILE = join(ROOT, 'hooks', 'dynamic-approvals.sh');
const HOUSEKEEPING_LOG = join(ROOT, 'logs', 'housekeeping.log');
const STATIC_HOOK = join(ROOT, 'hooks', 'pre-tool-use.sh');

// Minimum occurrences before a pattern is considered for auto-approve
const MIN_OCCURRENCES = 3;
// Minimum approval rate (0-1)
const MIN_APPROVAL_RATE = 0.9;
// Minimum average confidence
const MIN_AVG_CONFIDENCE = 0.85;

function log(msg) {
    const line = `[${new Date().toISOString()}] ${msg}`;
    console.log(line);
    try {
        appendFileSync(HOUSEKEEPING_LOG, line + '\n');
    } catch {}
}

function readHistory() {
    if (!existsSync(HISTORY_FILE)) {
        log('No eval history file found');
        return [];
    }
    const lines = readFileSync(HISTORY_FILE, 'utf-8').split('\n').filter(Boolean);
    return lines.map(line => {
        try { return JSON.parse(line); } catch { return null; }
    }).filter(Boolean);
}

function extractCommandPattern(entry) {
    // Only analyze Bash commands
    if (entry.tool !== 'Bash') return null;

    let cmd = entry.command || '';

    // Strip safe prefixes (same normalization as the hook)
    let prev;
    do {
        prev = cmd;
        cmd = cmd.replace(/^export\s+[A-Za-z_]\w*=[^;&]*[;&]+\s*/, '');
        cmd = cmd.replace(/^[A-Za-z_]\w*="[^"]*"\s+/, '');
        cmd = cmd.replace(/^[A-Za-z_]\w*='[^']*'\s+/, '');
        cmd = cmd.replace(/^[A-Za-z_]\w*=[^\s;&]*\s+/, '');
        cmd = cmd.replace(/^cd\s+[^;&]+[;&]+\s*/, '');
        cmd = cmd.replace(/^sleep\s+\d+\s*[;&]+\s*/, '');
    } while (cmd !== prev);

    // Extract first token (the command name)
    const match = cmd.match(/^([^\s]+)/);
    if (!match) return null;

    let firstToken = match[1];

    // Normalize paths to just the binary name
    // e.g., /usr/bin/python3 -> python3, .venv/bin/python -> python
    if (firstToken.includes('/')) {
        firstToken = firstToken.split('/').pop();
    }

    return firstToken;
}

function readExistingStaticRules() {
    // Read the static hook to find what patterns are already auto-approved
    if (!existsSync(STATIC_HOOK)) return new Set();
    const content = readFileSync(STATIC_HOOK, 'utf-8');
    const patterns = new Set();

    // Extract command names from grep patterns like '^(ssh|scp|rsync)\s'
    const matches = content.matchAll(/grep\s+-qE\s+['"]\^?\(?([^)'"]+)\)?/g);
    for (const m of matches) {
        const tokens = m[1].split('|').map(t => t.replace(/\\s.*/, '').replace(/[()]/g, ''));
        tokens.forEach(t => patterns.add(t));
    }
    return patterns;
}

function readExistingDynamicRules() {
    if (!existsSync(DYNAMIC_RULES_FILE)) return new Set();
    const content = readFileSync(DYNAMIC_RULES_FILE, 'utf-8');
    const patterns = new Set();
    const matches = content.matchAll(/# Pattern: (.+)/g);
    for (const m of matches) {
        patterns.add(m[1]);
    }
    return patterns;
}

function generateDynamicRules(newPatterns) {
    const lines = [
        '# Dynamic auto-approve rules — generated by eval-housekeeping.js',
        '# DO NOT EDIT MANUALLY — this file is regenerated periodically',
        `# Last updated: ${new Date().toISOString()}`,
        '#',
        '# These patterns were learned from eval history: commands that were',
        '# consistently approved by AI eval with high confidence.',
        '',
    ];

    // Read existing dynamic rules to preserve them
    const allPatterns = new Map();

    // Keep existing patterns
    if (existsSync(DYNAMIC_RULES_FILE)) {
        const content = readFileSync(DYNAMIC_RULES_FILE, 'utf-8');
        const blocks = content.split(/(?=# Pattern:)/);
        for (const block of blocks) {
            const patMatch = block.match(/# Pattern: (.+)/);
            if (patMatch) {
                allPatterns.set(patMatch[1], block.trim());
            }
        }
    }

    // Add new patterns
    for (const [pattern, stats] of newPatterns) {
        if (!allPatterns.has(pattern)) {
            const block = [
                `# Pattern: ${pattern}`,
                `# Learned: ${new Date().toISOString()} | Occurrences: ${stats.count} | Avg confidence: ${(stats.avgConfidence * 100).toFixed(0)}% | Approval rate: ${(stats.approvalRate * 100).toFixed(0)}%`,
                `# Example: ${stats.example.substring(0, 120)}`,
                `if echo "$NORM_CMD" | grep -qE '^${escapeRegex(pattern)}(\\s|$)'; then`,
                `    auto_approve`,
                `fi`,
            ].join('\n');
            allPatterns.set(pattern, block);
        }
    }

    for (const [, block] of allPatterns) {
        lines.push(block);
        lines.push('');
    }

    return lines.join('\n');
}

function escapeRegex(str) {
    return str.replace(/[.*+?^${}()|[\]\\]/g, '\\$&');
}

function main() {
    log('=== Housekeeping run started ===');

    const history = readHistory();
    log(`Read ${history.length} eval history entries`);

    if (history.length === 0) {
        log('No history to analyze');
        return;
    }

    // Group by command pattern
    const patternStats = new Map();

    for (const entry of history) {
        const pattern = extractCommandPattern(entry);
        if (!pattern) continue;

        if (!patternStats.has(pattern)) {
            patternStats.set(pattern, {
                count: 0,
                approved: 0,
                denied: 0,
                totalConfidence: 0,
                example: entry.command || '',
            });
        }

        const stats = patternStats.get(pattern);
        stats.count++;
        if (entry.decision === 'approved') stats.approved++;
        else stats.denied++;
        stats.totalConfidence += (entry.confidence || 0);
        // Keep the shortest example as most representative
        if ((entry.command || '').length < stats.example.length) {
            stats.example = entry.command;
        }
    }

    // Filter to candidates
    const staticRules = readExistingStaticRules();
    const existingDynamic = readExistingDynamicRules();
    const newPatterns = new Map();

    for (const [pattern, stats] of patternStats) {
        // Skip if already covered
        if (staticRules.has(pattern) || existingDynamic.has(pattern)) continue;

        // Check thresholds
        if (stats.count < MIN_OCCURRENCES) continue;

        const approvalRate = stats.approved / stats.count;
        if (approvalRate < MIN_APPROVAL_RATE) continue;

        const avgConfidence = stats.totalConfidence / stats.count;
        if (avgConfidence < MIN_AVG_CONFIDENCE) continue;

        stats.approvalRate = approvalRate;
        stats.avgConfidence = avgConfidence;
        newPatterns.set(pattern, stats);
    }

    if (newPatterns.size === 0) {
        log('No new patterns found to add');
        log('=== Housekeeping run completed ===');
        return;
    }

    // Generate rules file
    const content = generateDynamicRules(newPatterns);
    writeFileSync(DYNAMIC_RULES_FILE, content);

    // Report what was learned
    for (const [pattern, stats] of newPatterns) {
        log(`NEW RULE: "${pattern}" — ${stats.count} occurrences, ${(stats.approvalRate * 100).toFixed(0)}% approved, ${(stats.avgConfidence * 100).toFixed(0)}% avg confidence`);
        log(`  Example: ${stats.example.substring(0, 150)}`);
    }

    log(`Generated ${newPatterns.size} new dynamic rules → ${DYNAMIC_RULES_FILE}`);
    log('=== Housekeeping run completed ===');

    // Also sync dynamic rules to .claude/hooks/ if it exists
    const dotClaudeHooksDir = join(ROOT, '.claude', 'hooks');
    try {
        const dotClaudeDynamic = join(dotClaudeHooksDir, 'dynamic-approvals.sh');
        writeFileSync(dotClaudeDynamic, content);
        log(`Synced dynamic rules to ${dotClaudeDynamic}`);
    } catch {}
}

main();
